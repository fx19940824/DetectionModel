import random
import glob
import os
import numpy as np


def get_square(img, pos):
    """Extract a left or a right square from ndarray shape : (H, W, C))"""
    h = img.shape[0]
    if pos == 0:
        return img[:, :h]
    else:
        return img[:, -h:]

def split_img_into_squares(img):
    return get_square(img, 0), get_square(img, 1)

def hwc_to_chw(img):
    return np.transpose(img, axes=[2, 0, 1])

def resize_and_crop(pilimg,  isize=448):
    img = pilimg.resize((isize, isize))
    return np.array(img)

def batch(iterable, batch_size):
    """Yields lists by batch"""
    b = []
    for i, t in enumerate(iterable):
        b.append(t)
        if (i + 1) % batch_size == 0:
            yield b
            b = []

    if len(b) > 0:
        yield b

def gen_train_val(train_dir, val_dir):
    train = [name.split('/')[-1] for name in glob.glob(os.path.join(train_dir, '*.*')) if 'mask' not in name]
    val = [name.split('/')[-1] for name in glob.glob(os.path.join(val_dir, '*.*')) if 'mask' not in name]
    return {'train': train, 'val': val}


def normalize(x):
    return x.astype(np.float32) / 255

def merge_masks(img1, img2, full_w):
    h = img1.shape[0]

    new = np.zeros((h, full_w), np.float32)
    new[:, :full_w // 2 + 1] = img1[:, :full_w // 2 + 1]
    new[:, full_w // 2 + 1:] = img2[:, full_w // 2 + 1:]

    return new


